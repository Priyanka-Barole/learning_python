# Que- Write a python program find area of circle.
# solution-
# Input-radius of circle
# output-area of circle
# process-
# 1) take a user difiend value radius of circle and  allocated its value in radius_of_circle.
# 2) Find area of circle by 22/7*(radius_of_circle)**2 and allocate its value in area_of_circle.
